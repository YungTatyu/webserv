#ifndef CONF_HPP
#define CONF_HPP

namespace config
{

/**
 * CONF_MAIN              0x00000001  ->  0000 0000 0000 0000 0000 0000 0000 0001
 * CONF_HTTP              0x00000002  ->  0000 0000 0000 0000 0000 0000 0000 0010
 * CONF_EVENTS            0x00000004  ->  0000 0000 0000 0000 0000 0000 0000 0100
 * CONF_HTTP_SERVER       0x00000008  ->  0000 0000 0000 0000 0000 0000 0000 1000
 * CONF_HTTP_LOCATION     0x00000010  ->  0000 0000 0000 0000 0000 0000 0001 0000
 * CONF_HTTP_LIMIT_EXPECT 0x00000020  ->  0000 0000 0000 0000 0000 0000 0010 0000
*/
enum CONTEXT
{
	CONF_MAIN = 0x00000001,
	CONF_HTTP = 0x00000002,
	CONF_EVENTS = 0x00000004,
	CONF_HTTP_SERVER = 0x00000008,
	CONF_HTTP_LOCATION = 0x000000010,
	CONF_HTTP_LIMIT_EXCEPT = 0x00000020,
};

/**
 * CONF_NOARGS      0x00000001  ->  0000 0000 0000 0000 0000 0000 0000 0001
 * CONF_TAKE1       0x00000002  ->  0000 0000 0000 0000 0000 0000 0000 0010
 * CONF_TAKE2       0x00000004  ->  0000 0000 0000 0000 0000 0000 0000 0100
 * CONF_TAKE3       0x00000008  ->  0000 0000 0000 0000 0000 0000 0000 1000
 * CONF_TAKE4       0x00000010  ->  0000 0000 0000 0000 0000 0000 0001 0000
 * CONF_TAKE5       0x00000020  ->  0000 0000 0000 0000 0000 0000 0010 0000
 * CONF_TAKE6       0x00000040  ->  0000 0000 0000 0000 0000 0000 0100 0000
 * CONF_TAKE7       0x00000080  ->  0000 0000 0000 0000 0000 0000 1000 0000
 * CONF_1MORE       0x00000100  ->  0000 0000 0000 0000 0000 0001 0000 0000
 * CONF_2MORE       0x00000200  ->  0000 0000 0000 0000 0000 0010 0000 0000
*/
enum ARGS
{
	CONF_NOARGS = 0x00000001,
	CONF_TAKE1 = 0x00000002,
	CONF_TAKE2 = 0x00000004,
	CONF_TAKE3 = 0x00000008,
	CONF_TAKE4 = 0x00000010,
	CONF_TAKE5 = 0x00000020,
	CONF_TAKE6 = 0x00000040,
	CONF_TAKE7 = 0x00000080,
	CONF_1MORE = 0x00000100, // takes one or more arguments
	CONF_2MORE = 0x00000200, // takes two or more arguments
	CONF_TAKE12 = CONF_TAKE1|CONF_TAKE2, // takes one or two arguments
	CONF_TAKE123 = CONF_TAKE1|CONF_TAKE2|CONF_TAKE3, // takes one, two or three arguments
	CONF_TAKE1234 = CONF_TAKE1|CONF_TAKE2|CONF_TAKE3|CONF_TAKE4, // takes one, two, three or four arguments
};

} // namespace config


#endif